#include "3-calc.h"

/**
 * op_add - function to add two numbers
 * @a: first number
 * @b: Second number
 * Return: Sum of the numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract two numbers
 * @a: first number
 * @b: Second number
 * Return: Subtraction of the numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply two numbers
 * @a: first number
 * @b: Second number
 * Return: multiplication of the numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide two numbers
 * @a: first number
 * @b: Second number
 * Return: division of the numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to find modulo of two numbers
 * @a: first number
 * @b: Second number
 * Return: modulo of the numbers
 */

int op_mod(int a, int b)
{
	return (a % b);
}

